package dataflow;

public class Mahasiswa {
    public String nim;
    public String nama;
    public double ipk;
    public int semester;

    public Mahasiswa() {}

    public Mahasiswa(String nim, String nama, double ipk, int semester) {
        this.nim = nim;
        this.nama = nama;
        this.ipk = ipk;
        this.semester = semester;
    }
}
