package dataflow;

public class Dosen {
    public String nip;
    public String nama;
    public int lama;
    public int publikasi;
    public String email;

    public Dosen() {}

    public Dosen(String nip, String nama, int lama, int publikasi, String email) {
        this.nip = nip;
        this.nama = nama;
        this.lama = lama;
        this.publikasi = publikasi;
        this.email = email;
    }
}
